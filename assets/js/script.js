document.onkeydown = function (e) {
    if (e.keyCode == 123) {
        return false;
    }
    if (e.ctrlKey && e.shiftKey && e.keyCode == 'I'.charCodeAt(0)) {
        return false;
    }
    if (e.ctrlKey && e.shiftKey && e.keyCode == 'C'.charCodeAt(0)) {
        return false;
    }
    if (e.ctrlKey && e.shiftKey && e.keyCode == 'J'.charCodeAt(0)) {
        return false;
    }
    if (e.ctrlKey && e.keyCode == 'U'.charCodeAt(0)) {
        return false;
    }
}

// Get the skills section and all the skill bars
const skillsSection = document.querySelector('#skills');
const skillBars = document.querySelectorAll('.skill-bar');

// Define the width for each skill in percentages
const skillWidths = {
    html: '90%',
    css: '80%',
    javascript: '85%',
    bootstrap: '80%',
    jquery: '70%',
    react: '75%',
    sass: '70%',
    nodejs: '80%',
    express: '75%',
    mongodb: '70%',
};

// Function to animate the skill bars when the section is in view
function animateSkills() {
    // Get the position of the skills section relative to the viewport
    const sectionTop = skillsSection.getBoundingClientRect().top;

    // Check if the skills section is in view
    if (sectionTop <= window.innerHeight * 0.8) { // Trigger when 80% of section is in view
        skillBars.forEach((bar) => {
            // Get the class name of the skill and update the width accordingly
            const skillName = bar.classList[1]; // className is like 'html', 'css', etc.
            bar.style.width = skillWidths[skillName]; // Set width dynamically from the object
        });
    }
}

// Add event listener to the window for scroll and resize events
window.addEventListener('scroll', animateSkills);
window.addEventListener('resize', animateSkills);

// Initial check in case the user is already at the skills section
animateSkills();
